// 取当天的名言：用“年内第几天”做索引，保证一天内固定、每天轮换。
export function getDailyQuote(date = new Date()) {
  const start = new Date(date.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((date - start) / 86400000);
  return QUOTES[dayOfYear % QUOTES.length];
}
